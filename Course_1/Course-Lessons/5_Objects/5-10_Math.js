// Math

// Math object provides properties and methods for mathematical constants and functions
console.log(Math.PI); // Output: 3.141592653589793 (value of Ï€)
console.log(Math.E); // Output: 2.718281828459045 (value of e)
console.log(Math.SQRT2); // Output: 1.4142135623730951 (square root of 2)
console.log(Math.SQRT1_2); // Output: 0.7071067811865476 (square root of 1/2)
console.log(Math.LN2); // Output: 0.6931471805599453 (natural logarithm of 2)
console.log(Math.LN10); // Output: 2.302585092994046 (natural logarithm of 10)
console.log(Math.LOG2E); // Output: 1.4426950408889634 (base-2 logarithm of e)
console.log(Math.LOG10E); // Output: 0.4342944819032518 (base-10 logarithm of e)
console.log(Math.MAX_VALUE); // Output: 1.7976931348623157e+308 (largest representable number)
console.log(Math.MIN_VALUE); // Output: 5e-324 (smallest representable positive number)
console.log(Math.NEGATIVE_INFINITY); // Output: -Infinity (negative infinity)
console.log(Math.POSITIVE_INFINITY); // Output: Infinity (positive infinity)
console.log(Math.NaN); // Output: NaN (Not-a-Number)

// Math methods for calculations
console.log(Math.abs(-5)); // Output: 5 (absolute value)
console.log(Math.ceil(4.2)); // Output: 5 (rounds up to the nearest integer)
console.log(Math.floor(4.8)); // Output: 4 (rounds down to the nearest integer)
console.log(Math.round(4.5)); // Output: 5 (rounds to the nearest integer)
console.log(Math.max(1, 2, 3)); // Output: 3 (maximum value)
console.log(Math.min(1, 2, 3)); // Output: 1 (minimum value)
console.log(Math.pow(2, 3)); // Output: 8 (2 raised to the power of 3)
console.log(Math.sqrt(16)); // Output: 4 (square root of 16)

console.log(Math.random()); // Output: a random number between 0 (inclusive) and 1 (exclusive)
console.log(Math.random() * 10); // Output: a random number between 0 (inclusive) and 10 (exclusive)
console.log(Math.random() * 100); // Output: a random number between 0 (inclusive) and 100 (exclusive)
console.log(Math.random() * 100 + 1); // Output: a random number between 1 (inclusive) and 101 (exclusive)
