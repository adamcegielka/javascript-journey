// Addition
let sum = 5 + 3;
console.log(sum); // Output: 8

// Subtraction
let difference = 10 - 4;
console.log(difference); // Output: 6

// Multiplication
let product = 7 * 6;
console.log(product); // Output: 42

// Division
let quotient = 20 / 4;
console.log(quotient); // Output: 5

// Modulo
let remainder = 10 % 3;
console.log(remainder); // Output: 1

// Exponentiation
let power = 3 ** 2;
console.log(power); // Output: 9 (3 squared)

// Increment
let count = 5;
count++;
console.log(count); // Output: 6

// Decrement
let value = 10;
value--;
console.log(value); // Output: 9

// --------------------------

