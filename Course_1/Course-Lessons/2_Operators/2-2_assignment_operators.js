// Assignment
let x = 10;
console.log(x); // Output: 10

// Addition assignment
let y = 5;
y += 3; // Equivalent: y = y + 3
console.log(y); // Output: 8

// Subtraction assignment
let z = 10;
z -= 4; // Equivalent: z = z - 4
console.log(z); // Output: 6

// Multiplication assignment
let a = 4;
a *= 3; // Equivalent: a = a * 3
console.log(a); // Output: 12

// Division assignment
let b = 20;
b /= 4; // Equivalent: b = b / 4
console.log(b); // Output: 5

// Modulo assignment
let c = 11;
c %= 3; // Equivalent: c = c % 3
console.log(c); // Output: 2

// Exponentiation assignment
let d = 2;
d **= 3; // Equivalent: d = d ** 3 (2 raised to the power of 3)
console.log(d); // Output: 8

/*
=: Assigns a value to a variable.
+=: Adds a value to a variable and assigns the result.
-=: Subtracts a value from a variable and assigns the result.
*=: Multiplies a variable by a value and assigns the result.
/=: Divides a variable by a value and assigns the result.
%=: Computes the remainder of dividing a variable by a value and assigns the result.
**=: Raises a variable to the power of a value and assigns the result.
*/
