// Equal to
console.log(5 == "5"); // Output: true

// Strict equal to
console.log(5 === "5"); // Output: false
console.log(5 === 5); // Output: true

// Not equal to
console.log(10 != "10"); // Output: false
console.log(10 != 5); // Output: true

// Strict not equal to
console.log(10 !== "10"); // Output: true
console.log(10 !== 10); // Output: false

// Greater than
console.log(8 > 5); // Output: true
console.log(5 > 8); // Output: false

// Less than
console.log(3 < 7); // Output: true
console.log(7 < 3); // Output: false

// Greater than or equal to
console.log(10 >= 10); // Output: true
console.log(15 >= 10); // Output: true

// Less than or equal to
console.log(5 <= 5); // Output: true
console.log(4 <= 6); // Output: true

/*
==: Checks if values are equal (type conversion allowed).
===: Checks if values and types are strictly equal.
!=: Checks if values are not equal (type conversion allowed).
!==: Checks if values and/or types are not equal.
>: Checks if the left value is greater than the right value.
<: Checks if the left value is less than the right value.
>=: Checks if the left value is greater than or equal to the right value.
*/
